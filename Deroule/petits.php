<h2> Les p'tits guerriers </h2>

<p>
    Plusieurs activités sont proposés pour les enfants de 3 à 12 ans : <br>
</p>

<ul>
    <li>Une balle au prisonnier : partagé entre les 4 clans, nos chevaliers devront se lancer des boulets de canons ! (pas d'inquiétude, ce sont des balles en mousse)</li>
    <li>Un attrape-loup : il y a une équipe de garde et une équipe de voleur. Si le voleur se fait toucher, il devient alors un garde. Le dernier voleur à être touché deviendra le Roi des Voleurs !</li>
    <li>Un loup glacé : il y a une équipe de magicien et une équipe de voleur. Le voleur qui se fait toucher par un magicien devient glacé et sera en l'incapacité de bouger.</li>
    <li>Un jeu d'échec géant : les pions seront les enfants et les plus grands pourront y participer.</li>
    <li>Une initiation au combat médiévale : les enfants pourront se procurer une épée en mousse et apprendre à combattre entre eux.</li>
    <li>De l'équitation : les enfants pourront monter un cheval et s'occuper de lui.</li>
</ul>
