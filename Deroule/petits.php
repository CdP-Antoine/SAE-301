    <h2> Les p'tits guerriers </h2>

    <div class="image"><img src="Illustrations/Deroule/enfant1.JPG" alt="Enfants chevaliers"></div>

    <p>
        Plusieurs activités sont proposées pour les enfants de 3 à 12 ans : <br>
    </p>

    <ul>
        <li><u>Une balle au prisonnier</u> : partagé entre les 4 clans, nos chevaliers devront se lancer des boulets de canons ! (pas d'inquiétude, ce sont des balles en mousse)</li>
        <li><u>Un attrape-loup</u> : il y a une équipe de garde et une équipe de voleur. Si le voleur se fait toucher, il devient alors un garde. Le dernier voleur à être touché deviendra le Roi des Voleurs !</li>
        <li><u>Un loup glacé</u> : il y a une équipe de magicien et une équipe de voleur. Le voleur qui se fait toucher par un magicien devient glacé et sera en l'incapacité de bouger.</li>
        <li><u>Un jeu d'échec géant</u> : les pions seront les enfants et les plus grands pourront y participer.</li>
        <li><u>Une initiation au combat médiéval</u> : les enfants pourront se procurer une épée en mousse et apprendre à combattre entre eux.</li>
        <li><u>De l'équitation</u> : les enfants pourront monter un cheval et s'occuper de lui.</li>
    </ul>
